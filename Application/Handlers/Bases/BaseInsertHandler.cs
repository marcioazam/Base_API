using Domain.Interfaces.Repositories;
using MediatR;
using AutoMapper;
using Domain.Interfaces.ValueObjects;
using Domain.Interfaces.Entities.Base;
using Domain.Interfaces.Repositories.Base;
using Domain.Abstracts.Command.Base;
using Domain.ValueObjects.ResultInfo;
using Domain.Entities.Base;
using Application.Handlers.Default;
using Application.Interfaces.UoW;

namespace Application.Handlers.Bases
{
    public class BaseInsertHandler<TModel, TCommand>(IUnitOfWork unitOfWork, IRepositoryBase<TModel> repository, IMapper mapper) : BaseCommandHandler<TCommand, Result, IRepositoryBase<TModel>, TModel>(unitOfWork)
        where TModel : EntityBusinessRules, IEntity
        where TCommand : BaseInsertCommand, IRequest<Result>
    {
        private readonly IRepositoryBase<TModel> _repository = repository;
        private readonly IMapper _mapper = mapper;

        public override async Task<Result> Handle(TCommand command, CancellationToken cancellationToken)
        {
            TModel entity = await MapAndValidate(command);

            if (result.Failed())
                return result;

            result = await ExecuteOperationsDataBaseAndBRs(entity, command);

            return result;
        }

        private async Task<Result> ExecuteOperationsDataBaseAndBRs(TModel entity, TCommand command)
        {
            result = await entity.ExecuteBusinnesRulesBeforeOperations(command);

            if (result.Failed())
                return result;

            IAutoGeneratedValue entityId = await InsertIntoBD(entity);

            result = new Result(entityId, []);

            await entity.ExecuteBusinnesRulesAfterOperations(command);

            return result;
        }

        private async Task<IAutoGeneratedValue> InsertIntoBD(TModel entity)
        {
            var entityId = await _repository.Insert(entity);

            await Commit();
            return entityId;
        }

        private async Task<TModel> MapAndValidate(TCommand command)
        {
            var entity = _mapper.Map<TModel>(_mapper.Map<TModel>(command));

            await Validate(entity);

            return entity;
        }
    }
}
